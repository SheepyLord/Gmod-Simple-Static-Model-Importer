def detect_default_language() -> str:
    candidates = []
    try:
        default_locale = locale.getdefaultlocale()[0]
        if default_locale:
            candidates.append(default_locale)
    except Exception:
        pass
    try:
        current_locale = locale.getlocale()[0]
        if current_locale:
            candidates.append(current_locale)
    except Exception:
        pass

    for value in candidates:
        lowered = str(value).lower()
        if lowered.startswith("zh"):
            return "zh"
        if lowered.startswith("fr"):
            return "fr"
        if lowered.startswith("ja"):
            return "ja"
        if lowered.startswith("ko"):
            return "ko"
        if lowered.startswith("ru"):
            return "ru"
    return "en"


def tr(language_code: str, key: str, **kwargs) -> str:
    language_map = TRANSLATIONS.get(language_code) or TRANSLATIONS["en"]
    template = language_map.get(key) or TRANSLATIONS["en"].get(key) or key
    try:
        return template.format(**kwargs)
    except Exception:
        return template
